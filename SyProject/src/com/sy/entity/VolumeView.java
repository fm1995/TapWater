public class VolumeView {
	private String areaName;
	private Integer realityCount;
	private String volumeName;
	private String empName;
	private Integer mustCount;
	private Integer auditCount;
	private Integer volumeId;
	private Integer noCopy;
	private Integer noAuditCount;
	public String getAreaName() {
		return areaName;
	}
	public void setAreaName(String areaName) {
		this.areaName = areaName;
	}
	public Integer getRealityCount() {
		return realityCount;
	}
	public void setRealityCount(Integer realityCount) {
		this.realityCount = realityCount;
	}
	public String getVolumeName() {
		return volumeName;
	}
	public void setVolumeName(String volumeName) {
		this.volumeName = volumeName;
	}
	public String getEmpName() {
		return empName;
	}
	public void setEmpName(String empName) {
		this.empName = empName;
	}
	public Integer getMustCount() {
		return mustCount;
	}
	public void setMustCount(Integer mustCount) {
		this.mustCount = mustCount;
	}
	public Integer getAuditCount() {
		return auditCount;
	}
	public void setAuditCount(Integer auditCount) {
		this.auditCount = auditCount;
	}
	public Integer getVolumeId() {
		return volumeId;
	}
	public void setVolumeId(Integer volumeId) {
		this.volumeId = volumeId;
	}
	public Integer getNoCopy() {
		return noCopy;
	}
	public void setNoCopy(Integer noCopy) {
		this.noCopy = noCopy;
	}
	public Integer getNoAuditCount() {
		return noAuditCount;
	}
	public void setNoAuditCount(Integer noAuditCount) {
		this.noAuditCount = noAuditCount;
	}
	
}
